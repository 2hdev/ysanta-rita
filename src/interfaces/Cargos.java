    public void ListarCargos() {
        DefaultTableModel model = (DefaultTableModel) jtableCargos.getModel();
        ArrayList<modelos.Cargos> ListaCargos = dao.ListarCargos();
        ListaCargos.forEach((c) -> {
            model.addRow(new Object[]{c.getId_cargo(), c.getConcepto_cargo(), c.getMonto_cargo()});
        });
    }

